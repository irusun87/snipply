def analyze_and_generate(
    transcript: str,
    api_key: str = None,
    shorts_count: int = 3,
    titles_per_topic: int = 5,
    category: str = "감동"
) -> list[dict]:
    client = anthropic.Anthropic(api_key=api_key or os.getenv("ANTHROPIC_API_KEY"))

    # 선택된 카테고리 프롬프트 가져오기
    base_prompt = PROMPTS.get(category, PROMPTS["감동"])
    base_prompt = base_prompt.replace("{shorts_count}", str(shorts_count))
    base_prompt = base_prompt.replace("{titles_per_topic}", str(titles_per_topic))

    prompt = f"""{base_prompt}

---

아래 전사 내용을 분석해서 반드시 JSON 형식으로만 응답해주세요.

[JSON 출력 형식 - 반드시 이 형식만 출력, 다른 텍스트 금지]
{{
  "results": [
    {{
      "topic": "주제 한 줄 요약",
      "reason": "왜 시청자가 반응할지 1~2문장",
      "titles": ["제목1", "제목2", "제목3", "제목4", "제목5"],
      "script": "기승전결 대본 전문"
    }}
  ]
}}

[전사 내용]
{transcript}"""

    message = client.messages.create(
        model="claude-sonnet-4-5",
        max_tokens=4096,
        messages=[{"role": "user", "content": prompt}]
    )

    raw = message.content[0].text.strip()
    raw = re.sub(r"```json|```", "", raw).strip()

    try:
        data = json.loads(raw)
        return data["results"]
    except json.JSONDecodeError:
        return [{
            "topic": "분석 결과",
            "reason": "JSON 파싱에 실패했습니다. 아래 원문을 확인해주세요.",
            "titles": ["제목을 불러오지 못했습니다."],
            "script": raw
        }]
